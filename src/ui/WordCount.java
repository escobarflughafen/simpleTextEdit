    public static int wordcount(String str) {
        int count = 0;
        char[] strArr = str.toCharArray();
        for (int i = 0; i < strArr.length; i++) {
            if (strArr[i] == ' ' || strArr[i] == '\n' || strArr[i] == '\t')
                count++;
            else if (i == strArr.length - 1)
                count++;
        }


        return count;
    }

